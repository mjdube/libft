int			ft_isprint(unsigned char c)
{
	int output;

	if(c >= ' ' && c <= '~')
		output = 1;
	else if(c == '\r' || c == '\t' || c == '\f' || c == '\v' || c == '\n')
		output = 1;
	else 
		output = 0;

	return (output);
}
