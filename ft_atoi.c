int		ft_atoi(const char *str)
{
	int i;
	int pos;
	int neg;

	i = 0;
	neg = 1;
	pos = 0;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\v' || str[i] == '\r'
			|| str[i] == '\v' || str[i] == '\n')
		i++;
	if (str[i] == '-')
		neg = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		pos = pos * 10 + (str[i] - 48);
		i++;
	}
	return (pos * neg);
}
