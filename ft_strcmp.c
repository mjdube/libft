int		ft_strcmp(const char *s1, const char *s2)
{
	int i;
	int a;
	int b;

	i = 0;
	a = 0;
	b = 0;
	while (s1[i])
	{
		if (s1[i] != s2[i])
		{
			a = (int)s1[i];
			b = (int)s2[i];
			return (a - b);
		}
		i++;
	}
	return (0);
}
