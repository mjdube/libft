#include <string.h>

char		*ft_strrchr(const char *s, int c)
{
	int	i;
	int j;

	j = 1;
	i = 0;
	while (s[i])
		i++;
	while (s[i - j])
	{
		if (s[i - j] == (char)c)
		{
			return ((char*)&s[i - j]);
		}
		j++;
	}
	return (NULL);
}
