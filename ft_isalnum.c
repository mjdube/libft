int		ft_isalnum(int c)
{
	int output;

	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		output = 1;
	else if (c >= '0' && c <= '9')
		output = 1;
	else
		output = 0;
	return (output);
}
